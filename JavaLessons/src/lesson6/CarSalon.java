import java.util.*;
public class CarSalon {
	
	
	private ArrayList <Car> list1 = new ArrayList();
	
	public void add(Car car) {
		
		
		list1.add(car);
		
	}
	
	public void remove(Car car) {
		
		list1.remove(car);
		
	}
	
	
	public Car get(int i) {
		
		return list1.get(i);
		
	}
	
	
	public int size() {
		
		return list1.size();
		
	}
	
	
	
	
}

		

	
	
	
	
			
	
	
	
	

	
	
	
	
	
	
	
	

